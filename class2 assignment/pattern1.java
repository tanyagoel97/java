
package javatest;


public class pattern1 {
    public static void main(String[] args)
    {
        int n=1,i,j;
        for(i=1;i<=4;i++)
        {
            for(j=5-i;j>0;j--)
            {
                System.out.print(n);
            }
            System.out.print("\n");
        }
    }
    
}
